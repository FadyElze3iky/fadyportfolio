class Project {
  final String appName;
  final String description;
  final String github;
  final String logo;
  final List<String> screens;
  final String slogan;
  final bool vertical;

  Project({
    required this.appName,
    required this.description,
    required this.github,
    required this.logo,
    required this.screens,
    required this.slogan,
    required this.vertical,
  });
}
